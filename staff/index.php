	<h3>Isabelle Headrick, Executive Director</h3>
	<img src="../images/isabelleheadrick.jpg" alt="">
	<p>
		Isabelle Headrick has been involved in affordable housing since she first volunteered in a homeless women’s shelter as a teenager in Chicago. Before joining AHA! in 2012, she served for ten years as the Executive Director of Blackland Community Development Corporation, developing, managing and advocating for housing for people with disabilities, seniors, and homeless families. Isabelle has lived in Austin for twenty years and holds a B.A. from Oberlin College and an M.A. from the University of Texas at Austin. She has served as Co-Chair of the Austin Community Housing Development Organization Roundtable and participates on the Public Policy Committee of One Voice Central Texas. Outside of work, she swims, camps and feeds her children, which, as the mother of two teenage boys, is no small accomplishment. She loves the fact that AHA! is about more than housing: along with security, it helps people achieve independence, comfort, and dignity and allows them to participate in the life of their Austin neighborhoods.
	</p>

	<h3>Stephanie Thomas, President</h3>
	<img src="../images/stephaniethomas.jpg" alt="">
	<p>
		Stephanie Thomas has worked in disability rights and housing for nearly four decades.   As a person in a wheelchair, she knows firsthand the difference that accessible housing makes in improving one’s quality of life. Stephanie led the effort for the 1996 voluntary compliance agreement between ADAPT and the City of Austin to address widespread housing discrimination against people with disabilities: this led to Austin’s Visitability Ordinance, the accessibility in SMART housing, and the development of hundreds more accessible units in Austin. Prior to Austin, Stephanie worked with the Atlantis Community in Denver where she developed positive integrated housing options. In addition, she has authored numerous articles, white papers and other informational pieces on housing rights for people with disabilities.  
	</p>
	<p>
		As one of the founders of AHA!, Stephanie is proud of how the organization operates, partnering people with disabilities with people of various housing expertise to create viable integrated, accessible housing solutions that are affordable to low-income families and individuals.
	</p>

	<h3>Jennifer, McPhail, Vice President</h3>
	<img src="../images/jennifermcphail.jpg" alt="">
	<p>
		A life-long Austinite, Jennifer McPhail has served as a national and local community organizer for ADAPT for 23 years, working to improve long term services and  supports so that people with disabilities who need services can live in the community. She has also worked to improve sidewalk access throughout Austin, increase access to local businesses and increase the availability of affordable, accessible, integrated housing.  She has twice been appointed to the City of Austin’s Bond Advisory Committee and has volunteered with the Community Emergency Response Team. She served on the Texas Democratic Party Chairman’s Platform Advisory Committee in 2004, 2006,2008,  and 2010  and represented Senate District 14 at the 2004 National Convention. She joined the AHA! Board of Directors because she believes that the lack of affordable, accessible, integrated  housing  is one of the biggest problems the disability community faces now and in the future and  looks forward to more success for AHA! and its tenants. 
	</p>

	<h3>Paul Hilgers, Secretary</h3>
	<img src="../images/paulhilgers.jpg" alt="">
	<p>
		Paul Hilgers is CEO of the Austin Board of REALTORS®, a nearly 10,000-member professional organization that serves 18 Central Texas counties. Prior to his current position, Hilgers served nearly 10 years as the City of Austin’s Director of Neighborhood Housing and Community Development managing federal and local housing and community development programs in Austin, including the expansion of the Austin Housing Finance Corporation, which issues housing bonds and tax credits.  More recently he served three years as deputy general manager of the reorganized Pedernales Electric Cooperative (PEC) to help bring major changes to that organization. 
	</p>
	<p>
		An Austin native, Hilgers is a graduate of Whittier College and holds a Masters degree from the Lyndon B. Johnson School of Public Affairs at The University of Texas at Austin.   In addition to the City of Austin and the PEC, he has worked for former Governor Mark White, as a long-time district director for former Congressman Jake Pickle, and as a federal and community relations manager for the Lower Colorado River Authority.
	</p>

	<h3>Jean Langendorf, Treasurer</h3>
	<img src="../images/jeanlangendorf.jpg" alt="">
	<p>
		Jean Langendorf is the Vice President of Community and Housing Services at Easter Seals Central Texas. Prior to her current position, Jean was the Executive Director of United Cerebral Palsy of Texas (UCP Texas), which merged with Easter Seals in the beginning of 2010. During her tenure with UCP, she also served as the Project Director for the Texas Home of Your Own Coalition, a  project designed to expand homeownership opportunities to Texans with disabilities. Jean has served as a consultant providing technical assistance to Public Housing Authorities for Project Access and Fannie Mae, as well as to housing and homeownership programs throughout the United States. Jean was one of the disability advocates who helped to found Accessible Housing Austin! to address the great need for accessible and affordable housing in Austin.  She is thrilled to now be a member of the board and to see the growth the organization has experienced!
